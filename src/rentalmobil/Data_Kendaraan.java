public void getData(){
     //menghapus isi table tblGaji
     kendaraan.getDataVector( ).removeAllElements( );
     kendaraan.fireTableDataChanged( );


     try{
           //membuat statemen pemanggilan data pada table tblGaji dari database
           Statement stat = (Statement)koneksi.getKoneksi().createStatement();
           String sql        = "Select * from kendaraan";
           ResultSet res   = stat.executeQuery(sql);

           //penelusuran baris pada tabel tblGaji dari database
           while(res.next ()){
                Object[ ] obj = new Object[7];
                obj[0] = res.getString("NOPLAT"); 
                obj[1] = res.getString("IDTYPE");
                obj[2] = res.getString("KODEPEMILIK"); 
                obj[3] = res.getString("KODE_SERVICE");
                obj[4] = res.getString("TAHUN");
                obj[5] = res.getString("TARIFPERJAM");
                obj[6] = res.getString("STATUSRENTAL");
             

                kendaraan.addRow(obj);
            }
      }catch(SQLException err){
            JOptionPane.showMessageDialog(null, err.getMessage() );
      }
}

