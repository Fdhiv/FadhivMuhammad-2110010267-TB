    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Ambil indeks baris yang dipilih
int selectedRow = Tabel_Kendaraan.getSelectedRow();

// Validasi apakah baris dipilih
if (selectedRow == -1) {
    JOptionPane.showMessageDialog(this, "Pilih baris yang akan diubah!");
    return;
}

// Ambil data dari inputan
    String plat = txt_plat.getText();
    String type = txt_type.getText();
    String pemilik = txt_pemilik.getText();
    String service = txt_service.getText();
    String tahun = txt_tahun.getText();
    String tarif = txt_tarif.getText();
    String status = txt_status.getText();

// Validasi inputan (opsional)
if (plat.isEmpty() || type.isEmpty() || pemilik.isEmpty() || service.isEmpty() || tahun.isEmpty() || tarif.isEmpty() || status.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Isi semua kolom!");
    return;
}

// Perbarui data pada tabel
    DefaultTableModel model = (DefaultTableModel) Tabel_Kendaraan.getModel();
    model.setValueAt(plat, selectedRow, 0); // Ganti indeks kolom sesuai dengan struktur tabel
    model.setValueAt(type, selectedRow, 1);
    model.setValueAt(pemilik, selectedRow, 2);
    model.setValueAt(service, selectedRow, 3);
    model.setValueAt(tahun, selectedRow, 4);
    model.setValueAt(tarif, selectedRow, 5);
    model.setValueAt(status, selectedRow, 6);

    // Bersihkan inputan setelah diubah
    txt_plat.setText("");
    txt_type.setText("");
    txt_pemilik.setText("");
    txt_service.setText("");
    txt_tahun.setText("");
    txt_tarif.setText("");
    txt_status.setText("");

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       // Ambil indeks baris yang dipilih
int selectedRow = Tabel_Kendaraan.getSelectedRow();

// Validasi apakah baris dipilih
if (selectedRow == -1) {
    JOptionPane.showMessageDialog(this, "Pilih baris yang akan dihapus!");
    return;
}

// Konfirmasi pengguna untuk menghapus data
int option = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);

if (option == JOptionPane.YES_OPTION) {
    // Hapus data dari tabel
    DefaultTableModel model = (DefaultTableModel) Tabel_Kendaraan.getModel();
    model.removeRow(selectedRow);

    // Bersihkan inputan setelah dihapus (opsional)
    txt_plat.setText("");
    txt_type.setText("");
    txt_pemilik.setText("");
    txt_service.setText("");
    txt_tahun.setText("");
    txt_tarif.setText("");
    txt_status.setText("");
}

    }//GEN-LAST:event_jButton3ActionPerformed

